package chess.pieces;

/**
 *
 * @author Sijar.Ahmed
 */
class Pawns extends ChessPiece{

    /**
     * 
     * @param postion
     * @param color
     */
    public Pawns(CoOrdinate postion, int color) {
        this.color = color;
        this.position = postion;
    }

    @Override
    public String toString() {
        return "(P)" + position.toString();
    }

}
